
'''Problem 4
Temperature of a city in Fahrenheit degrees is input through the keyboard.
Write a program to convert this temperature into Centigrade degrees.'''

#Program to convert Fahrenheit to Centigrade degree
user_input=int(input("Enter the temerature in degree Fahrenheit : "))
deg_cel=(user_input-32)*0.5556
print("The converted temperature is : ",deg_cel,"degree celcius \n")


