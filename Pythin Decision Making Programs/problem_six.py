'''Problem 6
If the ages of Ram, Shyam and Ajay are input through the keyboard, write a
program to determine the the youngest of three '''

rams_age=int(input("Enter the age of Ram : "))
shyams_age=int(input("Enter the age of Shyam : "))
ajays_age=int(input("Enter the age of Ajay : "))
print("The youngest among three is person with age : ",min(rams_age,shyams_age,ajays_age))


